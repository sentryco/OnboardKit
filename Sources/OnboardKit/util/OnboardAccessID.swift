import Foundation
/**
 * Accessibility identifiers
 */
public enum OnboardAccessID {
   /**
    * Dismiss button ID
    * - Description: The accessibility identifier for the dismiss button.
    */
   public static let dismissButtonID: String = "dismissButton"
   /**
    * Action button ID
    * - Description: The accessibility identifier for the action button.
    */
   public static let actionButtonID: String = "actionButton"
   /**
    * Page button ID
    * - Description: The accessibility identifier for the page button.
    */
   public static let pageButtonID: String = "pageButton"
}
